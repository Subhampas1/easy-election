def t(key: str, lang: str = "en") -> str:
    """Get a translated string by key and language code.

    Args:
        key: The translation key from TRANSLATIONS dict.
        lang: ISO 639-1 language code (default: 'en').

    Returns:
        Translated string, or English fallback if key/lang missing.
    """
    entry: Optional[dict[str, str]] = TRANSLATIONS.get(key)
    if entry is None:
        return key
    return entry.get(lang, entry.get("en", key))


